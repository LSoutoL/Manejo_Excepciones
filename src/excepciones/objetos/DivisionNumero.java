public class DivisionNumero {
    private String n;
private String n2;

    public DivisionNumero() {
    }

    public DivisionNumero(String n, String n2) {
        this.n = n;
        this.n2 = n2;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getN2() {
        return n2;
    }

    public void setN2(String n2) {
        this.n2 = n2;
    }
    
}
